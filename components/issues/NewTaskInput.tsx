"use client"

import { Image as ImageIcon, Loader2 } from "lucide-react"
import { useRouter } from "next/navigation"
import { useEffect, useRef, useState, useTransition } from "react"

import VoiceDictationButton from "@/components/common/VoiceDictationButton"
import { Button } from "@/components/ui/button"
import { Textarea } from "@/components/ui/textarea"
import { ToastAction } from "@/components/ui/toast"
import {
  Tooltip,
  TooltipContent,
  TooltipProvider,
  TooltipTrigger,
} from "@/components/ui/tooltip"
import { createIssueAction } from "@/lib/actions/createIssue"
import { toast } from "@/lib/hooks/use-toast"
import { IssueTitleResponseSchema } from "@/lib/types/api/schemas"
import type { RepoFullName } from "@/lib/types/github"
import { mapGithubErrorToCopy } from "@/lib/ui/errorMessages"

interface Props {
  repoFullName: RepoFullName | null
  issuesEnabled?: boolean
}

export default function NewTaskInput({
  repoFullName,
  issuesEnabled = true,
}: Props) {
  const [description, setDescription] = useState("")
  const [loading, setLoading] = useState(false)
  const [generatingTitle, setGeneratingTitle] = useState(false)
  const [uploadingImage, setUploadingImage] = useState(false)
  // useTransition is useful for UI updates (e.g. router.refresh).
  // We should NOT perform remote/network work inside the transition callback
  // because any thrown error will escape the surrounding try/catch resulting
  // in an unhandled runtime error. Instead we do the async work first and
  // then transition the UI update.
  const [isPending, startTransition] = useTransition()

  const router = useRouter()
  const formRef = useRef<HTMLFormElement>(null)
  const fileInputRef = useRef<HTMLInputElement>(null)
  const [isMac, setIsMac] = useState(false)

  // Detect OS to adjust keyboard shortcut hint and behavior
  useEffect(() => {
    if (typeof window === "undefined") return
    const ua = window.navigator.userAgent || ""
    const platform =
      (window.navigator as unknown as { platform?: string }).platform || ""
    setIsMac(/mac/i.test(ua) || /mac/i.test(platform))
  }, [])

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault()

    if (!repoFullName) {
      toast({
        title: "No repository selected",
        description: "Please select a repository to create an issue.",
        variant: "destructive",
      })
      return
    }

    if (!issuesEnabled) {
      const { owner, repo } = repoFullName
      toast({
        title: "Issues are disabled",
        description: (
          <span>
            GitHub Issues are disabled for this repository.{" "}
            <a
              href={`https://github.com/${owner}/${repo}/settings#features`}
              target="_blank"
              rel="noopener noreferrer"
              className="underline"
            >
              Enable in GitHub settings
            </a>
            .
          </span>
        ),
        variant: "destructive",
      })
      return
    }

    if (!description.trim()) {
      toast({
        title: "Description required",
        description: "Please enter a description for your task.",
        variant: "destructive",
      })
      return
    }

    // Title will be auto-generated by an agent
    let taskTitle = ""
    try {
      setGeneratingTitle(true)
      const res = await fetch("/api/playground/issue-title", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ description }),
      })
      const data = await res.json()
      const parsedData = IssueTitleResponseSchema.safeParse(data)

      if (!parsedData.success) {
        throw new Error(parsedData.error.message)
      }

      taskTitle = parsedData.data.title.trim()
      if (!taskTitle) throw new Error("Received empty title from agent")
    } catch (err: unknown) {
      toast({
        title: "Error generating title",
        description: String(err),
        variant: "destructive",
      })
      setGeneratingTitle(false)
      return
    } finally {
      setGeneratingTitle(false)
    }

    setLoading(true)

    const { repo, owner } = repoFullName
    try {
      const result = await createIssueAction({
        repo,
        owner,
        title: taskTitle,
        body: description,
      })

      if (result.status === "success") {
        toast({
          title: "Task synced to GitHub",
          description: `Created: ${taskTitle}`,
          variant: "default",
        })
        setDescription("")
        startTransition(() => {
          router.refresh()
        })
      } else {
        const message = mapGithubErrorToCopy(result)
        const descriptionNode =
          result.code === "IssuesDisabled" ? (
            <span>
              {message}{" "}
              <a
                href={`https://github.com/${owner}/${repo}/settings#features`}
                target="_blank"
                rel="noopener noreferrer"
                className="underline"
              >
                Open GitHub settings
              </a>
              .
            </span>
          ) : (
            message
          )

        toast({
          title: "Error creating task",
          description: descriptionNode,
          variant: "destructive",
          action: <ToastAction altText="Dismiss">Dismiss</ToastAction>,
        })
      }
    } catch (err) {
      const message = err instanceof Error ? err.message : String(err)
      toast({
        title: "Error creating task",
        description: message,
        variant: "destructive",
      })
    } finally {
      setLoading(false)
    }
  }

  const isSubmitting = loading || generatingTitle || isPending
  const isDisabled = isSubmitting || !issuesEnabled

  const tooltipMessage = !issuesEnabled
    ? "Issues are disabled for this repository. Enable them in GitHub settings."
    : undefined

  const shortcutHint = isMac ? "⌘ + Enter" : "Ctrl + Enter"

  const handleKeyDown: React.KeyboardEventHandler<HTMLTextAreaElement> = (
    e
  ) => {
    const isSubmitCombo =
      e.key === "Enter" && ((isMac && e.metaKey) || (!isMac && e.ctrlKey))
    if (isSubmitCombo) {
      e.preventDefault()
      // Trigger form submission which uses the existing submit handler
      formRef.current?.requestSubmit()
    }
  }

  const uploadImage = async (file: File) => {
    if (!repoFullName) {
      toast({
        title: "No repository selected",
        description: "Select a repository before attaching images.",
        variant: "destructive",
      })
      return
    }

    try {
      setUploadingImage(true)
      const fd = new FormData()
      fd.append("file", file)
      fd.append("repoFullName", `${repoFullName.owner}/${repoFullName.repo}`)

      const res = await fetch("/api/images/upload", {
        method: "POST",
        body: fd,
      })

      if (!res.ok) {
        const errBody = await res.json().catch(() => ({}))
        throw new Error(errBody?.error ?? "Failed to upload image")
      }

      const data = (await res.json()) as { url: string; markdown: string }
      const snippet = data.markdown || `![](${data.url})`
      setDescription((prev) => (prev.trim() ? `${prev}\n\n${snippet}` : snippet))
      toast({ title: "Image attached", description: "Added to description." })
    } catch (err) {
      const message = err instanceof Error ? err.message : String(err)
      toast({
        title: "Image upload failed",
        description: message,
        variant: "destructive",
      })
    } finally {
      setUploadingImage(false)
    }
  }

  const handlePaste: React.ClipboardEventHandler<HTMLTextAreaElement> = async (
    e
  ) => {
    if (isDisabled) return
    const items = e.clipboardData?.items
    if (!items) return
    for (let i = 0; i < items.length; i++) {
      const item = items[i]
      if (item.kind === "file" && item.type.startsWith("image/")) {
        const file = item.getAsFile()
        if (file) {
          e.preventDefault()
          await uploadImage(file)
          return
        }
      }
    }
  }

  return (
    <TooltipProvider>
      <form
        onSubmit={handleSubmit}
        ref={formRef}
        className="mb-6 grid gap-4 border-b border-muted pb-6"
      >
        <div className="grid gap-2">
          <Tooltip open={!issuesEnabled ? undefined : false}>
            <TooltipTrigger asChild>
              <div>
                <Textarea
                  id="description"
                  value={description}
                  onChange={(e) => setDescription(e.target.value)}
                  onKeyDown={handleKeyDown}
                  onPaste={handlePaste}
                  placeholder={
                    !issuesEnabled
                      ? "Issues are disabled for this repository. Enable them in GitHub settings."
                      : "Describe a task or use the microphone to speak..."
                  }
                  required
                  disabled={isDisabled}
                  // Height tweaks: 50% viewport on mobile, 40% on md+ screens
                  className="h-[50vh] md:h-[40vh]"
                />
              </div>
            </TooltipTrigger>
            {!issuesEnabled && tooltipMessage ? (
              <TooltipContent>{tooltipMessage}</TooltipContent>
            ) : null}
          </Tooltip>
        </div>
        <div className="flex flex-wrap items-center gap-x-4 gap-y-2">
          <Tooltip open={!issuesEnabled ? undefined : false}>
            <TooltipTrigger asChild>
              <div>
                <Button type="submit" disabled={isDisabled}>
                  {generatingTitle ? (
                    <>
                      <Loader2 className="mr-2 h-4 w-4 animate-spin" />{" "}
                      Generating issue title...
                    </>
                  ) : loading || isPending ? (
                    <>
                      <Loader2 className="mr-2 h-4 w-4 animate-spin" />{" "}
                      Creating...
                    </>
                  ) : (
                    <>
                      Create Github Issue
                      <span className="ml-2 text-xs text-primary-foreground">
                        {shortcutHint}
                      </span>
                    </>
                  )}
                </Button>
              </div>
            </TooltipTrigger>
            {!issuesEnabled && tooltipMessage ? (
              <TooltipContent>{tooltipMessage}</TooltipContent>
            ) : null}
          </Tooltip>

          <VoiceDictationButton
            onTranscribed={(text) =>
              setDescription((prev) =>
                prev.trim() ? `${prev}\n${text}` : text
              )
            }
            disabled={isDisabled}
          />

          <div className="flex items-center gap-2">
            <input
              ref={fileInputRef}
              type="file"
              accept="image/*"
              className="hidden"
              onChange={(e) => {
                const f = e.target.files?.[0]
                if (f) void uploadImage(f)
                // Reset input so selecting same file again triggers onChange
                if (fileInputRef.current) fileInputRef.current.value = ""
              }}
              disabled={isDisabled}
            />
            <Button
              type="button"
              variant="secondary"
              onClick={() => fileInputRef.current?.click()}
              disabled={isDisabled || uploadingImage}
            >
              {uploadingImage ? (
                <>
                  <Loader2 className="mr-2 h-4 w-4 animate-spin" /> Uploading…
                </>
              ) : (
                <>
                  <ImageIcon className="mr-2 h-4 w-4" /> Attach image
                </>
              )}
            </Button>
          </div>
        </div>
      </form>
    </TooltipProvider>
  )
}

